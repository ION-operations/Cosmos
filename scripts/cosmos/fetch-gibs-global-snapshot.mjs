#!/usr/bin/env node
import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, extname, resolve } from 'node:path';

const endpoint = process.env.COSMOS_GIBS_WMS_ENDPOINT ?? 'https://gibs.earthdata.nasa.gov/wms/epsg4326/best/wms.cgi';
const layer = process.env.COSMOS_GIBS_LAYER ?? 'MODIS_Terra_CorrectedReflectance_TrueColor';
const time = process.env.COSMOS_GIBS_TIME ?? '2012-07-09';
const width = Number(process.env.COSMOS_GIBS_WIDTH ?? 2048);
const height = Number(process.env.COSMOS_GIBS_HEIGHT ?? 1024);
const format = process.env.COSMOS_GIBS_FORMAT ?? 'image/jpeg';
const bbox = process.env.COSMOS_GIBS_BBOX ?? '-180,-90,180,90';
const srs = process.env.COSMOS_GIBS_SRS ?? 'EPSG:4326';
const styles = process.env.COSMOS_GIBS_STYLES ?? '';

const extension = format.includes('png') ? '.png' : '.jpg';
const outputPath = resolve(process.cwd(), process.argv[2] ?? `public/cosmos/gibs/global-truecolor${extension}`);
const manifestPath = resolve(process.cwd(), process.argv[3] ?? 'public/cosmos/gibs/global-truecolor.manifest.json');
const publicTextureUrl = process.env.COSMOS_GIBS_PUBLIC_TEXTURE_URL ?? `/cosmos/gibs/${outputPath.split('/').pop()}`;

if (!Number.isFinite(width) || !Number.isFinite(height) || width <= 0 || height <= 0) {
  throw new Error(`Invalid COSMOS_GIBS_WIDTH/COSMOS_GIBS_HEIGHT: ${width}x${height}`);
}
if (!extname(outputPath)) {
  throw new Error(`Output path must include an image extension: ${outputPath}`);
}

const url = new URL(endpoint);
url.searchParams.set('SERVICE', 'WMS');
url.searchParams.set('VERSION', '1.1.1');
url.searchParams.set('REQUEST', 'GetMap');
url.searchParams.set('LAYERS', layer);
url.searchParams.set('STYLES', styles);
url.searchParams.set('SRS', srs);
url.searchParams.set('BBOX', bbox);
url.searchParams.set('WIDTH', String(width));
url.searchParams.set('HEIGHT', String(height));
url.searchParams.set('FORMAT', format);
url.searchParams.set('TIME', time);
url.searchParams.set('TRANSPARENT', 'FALSE');

const response = await fetch(url, {
  headers: {
    'User-Agent': 'Cosmos-WaterWorld/0.4 local global surface bootstrap',
  },
});

if (!response.ok) {
  throw new Error(`GIBS global WMS request failed: ${response.status} ${response.statusText} ${url}`);
}

const contentType = response.headers.get('content-type') ?? '';
if (!contentType.includes('image/')) {
  const body = await response.text();
  throw new Error(`GIBS global WMS returned non-image content-type ${contentType}: ${body.slice(0, 500)}`);
}

const bytes = new Uint8Array(await response.arrayBuffer());
const parsedBbox = bbox.split(',').map(Number);
const manifest = {
  schema: 'cosmos.gibs.globalSurfaceAtlas.v1',
  source: url.toString(),
  endpoint,
  layer,
  time,
  projection: srs,
  bbox: parsedBbox,
  width,
  height,
  format,
  textureUrl: publicTextureUrl,
  outputPath,
  bytes: bytes.byteLength,
  generatedAt: new Date().toISOString(),
  note: 'Generated by Cosmos R-0004. This global WMS atlas is blended into the /cosmos-review orbital planet surface pass.',
};

await mkdir(dirname(outputPath), { recursive: true });
await mkdir(dirname(manifestPath), { recursive: true });
await writeFile(outputPath, bytes);
await writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');

console.log(JSON.stringify({ source: url.toString(), outputPath, manifestPath, textureUrl: publicTextureUrl, bytes: bytes.byteLength }, null, 2));
